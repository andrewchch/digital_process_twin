"""
process_model.py
================
A code-as-process-model framework for defining, validating, executing,
and generating representations of business processes.

Design principles:
  - The code IS the canonical process definition.
  - Gaps are NotImplementedError bodies or explicit raise_gap() calls.
  - WorkItems are first-class objects surfaced by validate().
  - Diagrams and reports are generated by reflecting on the class structure.
  - All actors, steps, and data objects are subclasses of typed base classes.

Usage:
  process = DocumentApprovalProcess()
  process.validate()           # surface gaps as WorkItems
  process.report()             # print gap report to stdout
  process.generate_mermaid()   # print a Mermaid flowchart to stdout
  process.run(context)         # execute the process with a real/mock context
"""

from __future__ import annotations

import inspect
import textwrap
from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from typing import Any, Optional, Type


# ---------------------------------------------------------------------------
# Work Item — the collaboration primitive
# ---------------------------------------------------------------------------

class WorkItemStatus(Enum):
    OPEN       = "open"
    PROPOSED   = "proposed"   # AI or human has suggested a resolution
    RESOLVED   = "resolved"
    WONT_FIX   = "wont_fix"


class WorkItemType(Enum):
    GAP            = "gap"            # missing logic / undefined path
    QUESTION       = "question"       # ambiguity requiring a decision
    ASSUMPTION     = "assumption"     # something assumed, needs confirmation
    IMPROVEMENT    = "improvement"    # optional enhancement


@dataclass
class WorkItem:
    """
    A gap, question, assumption, or improvement discovered in the process.

    Work items are raised by Steps, Actors, or DataObjects via raise_gap().
    They accumulate on the owning Process instance and form the collaboration
    backlog that human and AI agents work through together.
    """
    id: str
    type: WorkItemType
    description: str
    raised_by: str                        # class name that raised this item
    linked_to: str                        # class name of the related element
    status: WorkItemStatus = WorkItemStatus.OPEN
    owner: Optional[str] = None           # agent assigned to resolve it
    resolution: Optional[str] = None      # how it was resolved
    raised_at: datetime = field(default_factory=datetime.now)

    def resolve(self, resolution: str, owner: str = "human") -> None:
        """Mark this work item as resolved with a description of the resolution."""
        self.resolution = resolution
        self.owner = owner
        self.status = WorkItemStatus.RESOLVED

    def propose(self, proposal: str, owner: str = "ai_agent") -> None:
        """Record a proposed resolution, pending human review."""
        self.resolution = proposal
        self.owner = owner
        self.status = WorkItemStatus.PROPOSED

    def __str__(self) -> str:
        tag = f"[{self.type.value.upper()}]"
        status = f"({self.status.value})"
        owner = f" → {self.owner}" if self.owner else ""
        return f"  {self.id} {tag} {status}{owner}\n    {self.linked_to}: {self.description}"


# ---------------------------------------------------------------------------
# Process — the orchestrator and collaboration surface
# ---------------------------------------------------------------------------

class Process(ABC):
    """
    Base class for all processes.

    A Process owns the ordered list of Steps, the participating Actors,
    and the accumulated WorkItems discovered during validation or execution.

    Subclasses define:
      - actors()  → list of Actor subclasses involved
      - steps()   → ordered list of Step subclasses in this process
      - name      → human-readable process name
      - description → what this process achieves
    """

    name: str = "Unnamed Process"
    description: str = ""

    def __init__(self) -> None:
        self._work_items: list[WorkItem] = []
        self._work_item_counter: int = 0

    # -- Subclass contract ---------------------------------------------------

    @abstractmethod
    def actors(self) -> list[Type[Actor]]:
        """Return the Actor subclasses that participate in this process."""
        ...

    @abstractmethod
    def steps(self) -> list[Type[Step]]:
        """Return the Step subclasses in execution order."""
        ...

    # -- Work item management ------------------------------------------------

    def raise_gap(
        self,
        description: str,
        raised_by: str,
        linked_to: str,
        type: WorkItemType = WorkItemType.GAP,
    ) -> WorkItem:
        """Create and register a new work item on this process."""
        self._work_item_counter += 1
        wi = WorkItem(
            id=f"WI-{self._work_item_counter:03d}",
            type=type,
            description=description,
            raised_by=raised_by,
            linked_to=linked_to,
        )
        self._work_items.append(wi)
        return wi

    @property
    def work_items(self) -> list[WorkItem]:
        return list(self._work_items)

    @property
    def open_work_items(self) -> list[WorkItem]:
        return [wi for wi in self._work_items if wi.status == WorkItemStatus.OPEN]

    # -- Validation ----------------------------------------------------------

    def validate(self) -> list[WorkItem]:
        """
        Reflect on all Steps and Actors to surface structural gaps.

        Checks performed:
          - Steps with no defined actor
          - Steps with no inputs or no outputs
          - Steps whose handle() body raises NotImplementedError
          - Steps with no preconditions defined (assumption raised)
          - Steps with no postconditions defined (assumption raised)
          - Actors with no steps assigned to them
          - DataObject states with no transitions defined
        """
        self._work_items.clear()
        self._work_item_counter = 0

        actor_classes = self.actors()
        step_classes = self.steps()

        # Track which actors have at least one step
        actors_with_steps: set[str] = set()

        for step_cls in step_classes:
            sname = step_cls.__name__

            # Actor assigned?
            if not step_cls.actor:
                self.raise_gap(
                    f"No actor assigned — who is responsible for this step?",
                    raised_by="Process.validate",
                    linked_to=sname,
                )
            else:
                actors_with_steps.add(step_cls.actor.__name__)

            # Inputs defined?
            if not step_cls.inputs:
                self.raise_gap(
                    f"No inputs defined — what data does this step consume?",
                    raised_by="Process.validate",
                    linked_to=sname,
                )

            # Outputs defined?
            if not step_cls.outputs:
                self.raise_gap(
                    f"No outputs defined — what data does this step produce?",
                    raised_by="Process.validate",
                    linked_to=sname,
                )

            # Preconditions?
            if not step_cls.preconditions:
                self.raise_gap(
                    f"No preconditions defined — what must be true before this step runs?",
                    raised_by="Process.validate",
                    linked_to=sname,
                    type=WorkItemType.ASSUMPTION,
                )

            # Postconditions?
            if not step_cls.postconditions:
                self.raise_gap(
                    f"No postconditions defined — what must be true after this step completes?",
                    raised_by="Process.validate",
                    linked_to=sname,
                    type=WorkItemType.ASSUMPTION,
                )

            # handle() implemented?
            if _is_not_implemented(step_cls, "handle"):
                self.raise_gap(
                    f"handle() is not implemented — process logic is missing.",
                    raised_by="Process.validate",
                    linked_to=sname,
                )

            # Error paths?
            if _is_not_implemented(step_cls, "on_error"):
                self.raise_gap(
                    f"on_error() is not implemented — what happens if this step fails?",
                    raised_by="Process.validate",
                    linked_to=sname,
                )

        # Actors with no steps
        for actor_cls in actor_classes:
            if actor_cls.__name__ not in actors_with_steps:
                self.raise_gap(
                    f"Actor has no steps assigned — is this role needed?",
                    raised_by="Process.validate",
                    linked_to=actor_cls.__name__,
                    type=WorkItemType.QUESTION,
                )

        return self.work_items

    # -- Execution -----------------------------------------------------------

    def run(self, context: dict[str, Any]) -> dict[str, Any]:
        """
        Execute the process steps in order against a context dictionary.

        The context acts as the shared data bus — steps read from it and
        write their outputs back to it.  In a real implementation each Step
        would also receive typed DataObject instances.
        """
        print(f"\n▶ Running: {self.name}")
        print("─" * 60)
        for step_cls in self.steps():
            step = step_cls(process=self)
            print(f"  → {step_cls.__name__}")
            try:
                context = step.execute(context)
            except NotImplementedError:
                print(f"     ⚠ NotImplementedError — step not yet implemented, skipping.")
            except Exception as exc:
                print(f"     ✗ Error: {exc}")
                try:
                    context = step.on_error(context, exc)
                except NotImplementedError:
                    print(f"     ⚠ on_error() not implemented — halting process.")
                    break
        print("─" * 60)
        print(f"✓ Process complete.\n")
        return context

    # -- Reporting -----------------------------------------------------------

    def report(self) -> None:
        """Print a human-readable gap report to stdout."""
        items = self.work_items
        if not items:
            self.validate()
            items = self.work_items

        open_items   = [wi for wi in items if wi.status == WorkItemStatus.OPEN]
        proposed     = [wi for wi in items if wi.status == WorkItemStatus.PROPOSED]
        resolved     = [wi for wi in items if wi.status == WorkItemStatus.RESOLVED]

        print(f"\n{'═'*60}")
        print(f"  PROCESS REVIEW REPORT: {self.name}")
        print(f"  {self.description}")
        print(f"{'═'*60}")
        print(f"  Steps:       {len(self.steps())}")
        print(f"  Actors:      {len(self.actors())}")
        print(f"  Work items:  {len(items)}  "
              f"(open: {len(open_items)}, proposed: {len(proposed)}, resolved: {len(resolved)})")
        print(f"{'─'*60}")

        by_type = {}
        for wi in items:
            by_type.setdefault(wi.type, []).append(wi)

        for wtype, wis in by_type.items():
            print(f"\n  {wtype.value.upper()}S ({len(wis)})")
            for wi in wis:
                print(str(wi))

        print(f"\n{'═'*60}\n")

    # -- Diagram generation --------------------------------------------------

    def generate_mermaid(self) -> str:
        """
        Generate a Mermaid flowchart string from the step definitions.

        The diagram is derived entirely from the class structure — no
        manual diagram maintenance is needed.  Re-run after any change
        to the process steps to get an updated diagram.
        """
        lines = ["flowchart TD"]
        lines.append(f'    %% {self.name}')
        lines.append(f'    START([Start]) --> {self.steps()[0].__name__}')

        step_classes = self.steps()
        for i, step_cls in enumerate(step_classes):
            sname  = step_cls.__name__
            label  = _camel_to_label(sname)
            actor  = step_cls.actor.__name__ if step_cls.actor else "Unassigned"
            inputs  = ", ".join(c.__name__ for c in step_cls.inputs)  if step_cls.inputs  else "—"
            outputs = ", ".join(c.__name__ for c in step_cls.outputs) if step_cls.outputs else "—"
            not_impl = _is_not_implemented(step_cls, "handle")

            shape_open  = "{{" if not_impl else "["
            shape_close = "}}" if not_impl else "]"

            lines.append(
                f'    {sname}{shape_open}"{label}<br/>'
                f'<small>{actor}</small>"{shape_close}'
            )
            lines.append(f'    %% inputs: {inputs} | outputs: {outputs}')

            if i < len(step_classes) - 1:
                next_cls = step_classes[i + 1]
                lines.append(f'    {sname} --> {next_cls.__name__}')

        lines.append(f'    {step_classes[-1].__name__} --> END([End])')

        # Swim-lane comment block
        lines.append("\n    %% Actor responsibilities:")
        for actor_cls in self.actors():
            assigned = [s.__name__ for s in step_classes if s.actor and s.actor.__name__ == actor_cls.__name__]
            if assigned:
                lines.append(f"    %% {actor_cls.__name__}: {', '.join(assigned)}")

        diagram = "\n".join(lines)
        return diagram

    def print_mermaid(self) -> None:
        """Print the Mermaid diagram to stdout, wrapped in a code fence."""
        print("\n```mermaid")
        print(self.generate_mermaid())
        print("```\n")


