class DocumentState(Enum):
    DRAFT      = "draft"
    SUBMITTED  = "submitted"
    IN_REVIEW  = "in_review"
    APPROVED   = "approved"
    REJECTED   = "rejected"
    PUBLISHED  = "published"
    ARCHIVED   = "archived"


class Document(DataObject):
    """
    The core content artefact flowing through the approval process.

    Carries the content, metadata, and current workflow state.  State
    transitions are strictly validated to prevent process bypasses.
    """

    states = DocumentState
    initial_state = DocumentState.DRAFT
    valid_transitions = {
        DocumentState.DRAFT:      [DocumentState.SUBMITTED],
        DocumentState.SUBMITTED:  [DocumentState.IN_REVIEW, DocumentState.DRAFT],
        DocumentState.IN_REVIEW:  [DocumentState.APPROVED, DocumentState.REJECTED],
        DocumentState.APPROVED:   [DocumentState.PUBLISHED],
        DocumentState.REJECTED:   [DocumentState.DRAFT],
        DocumentState.PUBLISHED:  [DocumentState.ARCHIVED],
        # GAP: Can a published document be re-submitted for revision?
        #      If so, PUBLISHED → DRAFT should be added here.
        DocumentState.ARCHIVED:   [],
    }

    def __init__(self, title: str = "", content: str = "") -> None:
        super().__init__()
        self.title = title
        self.content = content
        self.author: Optional[str] = None
        self.approver: Optional[str] = None
        self.rejection_reason: Optional[str] = None
        self.version: int = 1


class AccessPolicyLevel(Enum):
    PUBLIC       = "public"
    INTERNAL     = "internal"
    RESTRICTED   = "restricted"
    CONFIDENTIAL = "confidential"


class AccessPolicy(DataObject):
    """
    Defines who can read a published document.

    Decoupled from the Document itself so access rules can be updated
    independently of document content.
    """

    states = AccessPolicyLevel
    initial_state = AccessPolicyLevel.INTERNAL
    valid_transitions = {
        # Any level can be changed to any other — access policy is mutable.
        AccessPolicyLevel.PUBLIC:       [AccessPolicyLevel.INTERNAL, AccessPolicyLevel.RESTRICTED, AccessPolicyLevel.CONFIDENTIAL],
        AccessPolicyLevel.INTERNAL:     [AccessPolicyLevel.PUBLIC, AccessPolicyLevel.RESTRICTED, AccessPolicyLevel.CONFIDENTIAL],
        AccessPolicyLevel.RESTRICTED:   [AccessPolicyLevel.PUBLIC, AccessPolicyLevel.INTERNAL, AccessPolicyLevel.CONFIDENTIAL],
        AccessPolicyLevel.CONFIDENTIAL: [AccessPolicyLevel.PUBLIC, AccessPolicyLevel.INTERNAL, AccessPolicyLevel.RESTRICTED],
    }

    def __init__(self) -> None:
        super().__init__()
        self.allowed_groups: list[str] = []
        self.allowed_users: list[str] = []
        # GAP: Is there an expiry date on access policies?
        self.expires_at: Optional[datetime] = None


class Notification(DataObject):
    """
    A notification dispatched to an Actor during the process.

    Tracks delivery status.  The notification system itself is a gap —
    see NotificationState transitions below.
    """

    class NotificationState(Enum):
        PENDING   = "pending"
        SENT      = "sent"
        DELIVERED = "delivered"
        FAILED    = "failed"
        # GAP: Should failed notifications be retried?  If so, add RETRYING state.

    states = NotificationState
    initial_state = NotificationState.PENDING
    valid_transitions = {
        NotificationState.PENDING:   [NotificationState.SENT, NotificationState.FAILED],
        NotificationState.SENT:      [NotificationState.DELIVERED, NotificationState.FAILED],
        NotificationState.DELIVERED: [],
        NotificationState.FAILED:    [],
    }

    def __init__(self, recipient: str = "", subject: str = "", body: str = "") -> None:
        super().__init__()
        self.recipient = recipient
        self.subject = subject
        self.body = body


