class Actor(ABC):
    """
    Base class for all actors (roles) in a process.

    An actor represents a role, not a specific person.  The same person
    may fill multiple roles; multiple people may fill the same role.

    Subclasses define:
      - name        → display name of the role
      - description → what this role is responsible for in the process
      - perform()   → dispatch an action this actor can take
    """

    name: str = "Unnamed Actor"
    description: str = ""

    def perform(self, action: str, context: dict[str, Any]) -> dict[str, Any]:
        """
        Dispatch an action for this actor.

        Override in subclasses to implement actor-specific behaviour.
        Raise NotImplementedError for actions not yet defined.
        """
        raise NotImplementedError(
            f"{self.__class__.__name__}.perform('{action}') is not implemented."
        )

    def notify(self, message: str, context: dict[str, Any]) -> None:
        """
        Send a notification to this actor.

        Override to implement email, Slack, in-app notification, etc.
        """
        raise NotImplementedError(
            f"{self.__class__.__name__}.notify() is not implemented."
        )


