class AuthorActor(Actor):
    """
    The user who creates and submits documents for approval.

    Responsible for: creating drafts, editing rejected documents,
    and setting the initial access policy proposal.
    """

    name = "Author"
    description = "Creates, edits, and submits documents for approval."

    def perform(self, action: str, context: dict[str, Any]) -> dict[str, Any]:
        if action == "create_draft":
            doc = Document(
                title=context.get("title", "Untitled"),
                content=context.get("content", ""),
            )
            doc.author = context.get("user_id", "unknown")
            context["document"] = doc
            return context
        elif action == "edit_draft":
            doc: Document = context["document"]
            doc.content = context.get("new_content", doc.content)
            doc.version += 1
            return context
        elif action == "submit":
            doc: Document = context["document"]
            doc.transition(DocumentState.SUBMITTED)
            return context
        else:
            raise NotImplementedError(
                f"AuthorActor.perform('{action}') is not implemented."
            )

    def notify(self, message: str, context: dict[str, Any]) -> None:
        # GAP: Implement email / in-app notification to the author.
        raise NotImplementedError("AuthorActor.notify() is not implemented.")


class ApproverActor(Actor):
    """
    A designated reviewer who approves or rejects submitted documents.

    Responsible for: reviewing document content, checking compliance,
    approving or rejecting with a mandatory reason on rejection.

    Open questions:
      - How is an approver assigned? (role-based, document-category, manual)
      - What is the SLA for approval? (see WI-004 equivalent in validate())
      - Can an approver delegate to another approver?
    """

    name = "Approver"
    description = "Reviews submitted documents and approves or rejects them."

    def perform(self, action: str, context: dict[str, Any]) -> dict[str, Any]:
        if action == "approve":
            doc: Document = context["document"]
            doc.approver = context.get("approver_id", "unknown")
            doc.transition(DocumentState.APPROVED)
            return context
        elif action == "reject":
            doc: Document = context["document"]
            reason = context.get("rejection_reason")
            if not reason:
                raise ValueError("A rejection reason is required.")
            doc.rejection_reason = reason
            doc.transition(DocumentState.REJECTED)
            return context
        elif action == "request_changes":
            # GAP: 'request_changes' is a common workflow state between
            # IN_REVIEW and REJECTED — should it be a distinct step/state?
            raise NotImplementedError(
                "ApproverActor.perform('request_changes') is not implemented."
            )
        else:
            raise NotImplementedError(
                f"ApproverActor.perform('{action}') is not implemented."
            )

    def notify(self, message: str, context: dict[str, Any]) -> None:
        # GAP: Implement notification — email, Slack, or workflow system.
        raise NotImplementedError("ApproverActor.notify() is not implemented.")


