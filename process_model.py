    actor = ApproverActor
    inputs:  list[Type[DataObject]] = [Document]
    outputs: list[Type[DataObject]] = [Document, Notification]
    preconditions  = [
        "Document is in SUBMITTED or IN_REVIEW state.",
        "An approver has been assigned.",
        "Approver has 'approve document' permission.",
    ]
    postconditions = [
        "Document is in APPROVED or REJECTED state.",
        "Approver identity and decision timestamp are recorded.",
        "Author has been notified of the outcome.",
    ]
    description = "Approver reviews the document and makes an approve/reject decision."

    def handle(self, context: dict[str, Any]) -> dict[str, Any]:
        doc: Document = context["document"]
        doc.transition(DocumentState.IN_REVIEW)

        decision = context.get("approval_decision", "approve")
        approver = ApproverActor()
        context["approver_id"] = context.get("approver_id", "approver_001")

        if decision == "approve":
            context = approver.perform("approve", context)
