# Step base class
# ---------------------------------------------------------------------------

class Step(ABC):
    """
    Base class for all process steps.

    A Step is a discrete unit of work performed by one Actor, consuming
    defined inputs and producing defined outputs.  Pre- and post-conditions
    document the invariants that must hold around the step.

    Class-level attributes (set on subclasses, not instances):
      - actor           → the Actor subclass responsible for this step
      - inputs          → list of DataObject subclasses consumed
      - outputs         → list of DataObject subclasses produced
      - preconditions   → list of plain-English strings
      - postconditions  → list of plain-English strings
      - description     → what this step does

    Instance methods:
      - handle()   → the step's core logic (raise NotImplementedError if not yet defined)
      - on_error() → what to do if handle() raises an exception
    """

    actor: Optional[Type[Actor]] = None
    inputs:  list[Type[DataObject]] = []
    outputs: list[Type[DataObject]] = []
    preconditions:  list[str] = []
    postconditions: list[str] = []
    description: str = ""

    def __init__(self, process: Process) -> None:
        self.process = process

    def execute(self, context: dict[str, Any]) -> dict[str, Any]:
        """
        Run the step: check preconditions, call handle(), check postconditions.
        """
        self._check_preconditions(context)
        context = self.handle(context)
        self._check_postconditions(context)
        return context

    @abstractmethod
    def handle(self, context: dict[str, Any]) -> dict[str, Any]:
        """
        Core step logic.  Receives and returns the shared process context.

        Raise NotImplementedError if this step is not yet implemented —
        that signals a gap to validate() and run().
        """
        raise NotImplementedError(
            f"{self.__class__.__name__}.handle() is not yet implemented."
        )

    def on_error(self, context: dict[str, Any], error: Exception) -> dict[str, Any]:
        """
        Error handler called when handle() raises an exception.

        Override to implement retry logic, compensation steps, escalation, etc.
        Raise NotImplementedError to mark this as an open gap.
        """
        raise NotImplementedError(
            f"{self.__class__.__name__}.on_error() is not yet implemented."
        )

    def _check_preconditions(self, context: dict[str, Any]) -> None:
        """Override to add runtime precondition assertions."""
        pass

    def _check_postconditions(self, context: dict[str, Any]) -> None:
        """Override to add runtime postcondition assertions."""
        pass


