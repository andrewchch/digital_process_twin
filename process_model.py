# DataObject base class
# ---------------------------------------------------------------------------

class DataObject(ABC):
    """
    Base class for all data objects that flow through a process.

    A DataObject has a state machine: a defined set of states and valid
    transitions between them.  Attempts to make an invalid transition
    raise a ValueError, enforcing process integrity.

    Subclasses define:
      - states           → Enum class of valid states
      - initial_state    → the state a new instance starts in
      - valid_transitions → dict mapping each state to the states it can move to
    """

    states: Type[Enum]
    initial_state: Enum
    valid_transitions: dict[Enum, list[Enum]] = {}

    def __init__(self) -> None:
        self._state = self.initial_state
        self._history: list[tuple[Enum, datetime]] = [(self._state, datetime.now())]

    @property
    def state(self) -> Enum:
        return self._state

    def transition(self, new_state: Enum) -> None:
        """
        Move this object to a new state, validating the transition first.

        Raises ValueError if the transition is not permitted.
        """
        allowed = self.valid_transitions.get(self._state, [])
        if new_state not in allowed:
            raise ValueError(
                f"Invalid transition on {self.__class__.__name__}: "
                f"{self._state.value} → {new_state.value}. "
                f"Allowed: {[s.value for s in allowed]}"
            )
        self._state = new_state
        self._history.append((new_state, datetime.now()))

    @property
    def history(self) -> list[tuple[Enum, datetime]]:
        return list(self._history)


