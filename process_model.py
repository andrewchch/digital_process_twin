class DocumentApprovalProcess(Process):
    """
    End-to-end process for creating, approving, and publishing an intranet document.

    Flow:
      CreateDraft → EditAndFinaliseDraft → SubmitForApproval
        → ReviewDocument → [if approved] SetAccessPolicy → PublishDocument
        → [if rejected] EditAndFinaliseDraft (loop)

    Actors:   Author, Approver, Admin, System
    Objects:  Document, AccessPolicy, Notification

    Known gaps (see validate() for full list):
      - Approver assignment logic
      - 'Request changes' workflow state
      - Approval SLA and escalation path
      - Access policy ownership (Author vs Approver vs Admin)
      - Notification delivery mechanism
      - Rejection loop limit
      - Published document revision workflow
    """

    name = "Document Approval and Publishing Process"
    description = "Author creates → Approver reviews → Admin sets access → System publishes."

    def actors(self) -> list[Type[Actor]]:
        return [AuthorActor, ApproverActor, AdminActor, SystemActor]

    def steps(self) -> list[Type[Step]]:
        return [
            CreateDraft,
            EditAndFinaliseDraft,
            SubmitForApproval,
            ReviewDocument,
            SetAccessPolicy,
            PublishDocument,
        ]


